import math
from os import PathLike
from pathlib import Path

from .models import Line, Point, Point3D, Primitive, Scene, Sphere, Text


def _format_number(value: float) -> str:
    if not math.isfinite(value):
        raise ValueError("cannot write a non-finite number to Tcl")
    return format(value, ".12g")


def _format_point(point: Point3D) -> str:
    return "{" + " ".join(_format_number(value) for value in point) + "}"


def _tcl_string(value: str | Path) -> str:
    text = str(value)
    if "\n" in text or "\r" in text:
        raise ValueError("Tcl strings cannot contain line breaks")
    escaped = (
        text.replace("\\", "\\\\")
        .replace('"', '\\"')
        .replace("$", "\\$")
        .replace("[", "\\[")
        .replace("]", "\\]")
    )
    return f'"{escaped}"'


def _tcl_token(value: str) -> str:
    if not value or not all(
        character.isalnum() or character in {"_", "-"}
        for character in value
    ):
        raise ValueError(f"unsafe Tcl token: {value!r}")
    return value


def render_primitive(primitive: Primitive) -> str:
    """Translate one backend-independent primitive into a VMD Tcl command."""
    match primitive:
        case Sphere(center, radius, color, resolution):
            return (
                f"::bias_vizualization::sphere $m {_format_point(center)} "
                f"{_format_number(radius)} {_tcl_token(color)} {resolution}"
            )
        case Line(start, end, color, width, style):
            return (
                f"::bias_vizualization::line $m {_format_point(start)} {_format_point(end)} "
                f"{_tcl_token(color)} {width} {_tcl_token(style)}"
            )
        case Point(position, color):
            return (
                f"::bias_vizualization::point $m {_format_point(position)} "
                f"{_tcl_token(color)}"
            )
        case Text(position, label, size):
            return (
                f"::bias_vizualization::text $m {_format_point(position)} "
                f"{_tcl_string(label)} {_format_number(size)}"
            )
        case _:
            raise TypeError(f"unsupported scene primitive: {type(primitive).__name__}")


def render_tcl(
    scene: Scene,
    receptor_pdb: str | PathLike[str],
    renderer_tcl: str | PathLike[str],
    scene_name: str,
    graphics_opacity: float,
) -> str:
    """Render a Scene as a standalone Tcl script for VMD."""
    receptor_path = Path(receptor_pdb).resolve()
    renderer_path = Path(renderer_tcl).resolve()
    if not receptor_path.is_file():
        raise FileNotFoundError(f"receptor file does not exist: {receptor_path}")
    if not renderer_path.is_file():
        raise FileNotFoundError(f"renderer Tcl does not exist: {renderer_path}")
    if not math.isfinite(graphics_opacity) or not 0.0 < graphics_opacity <= 1.0:
        raise ValueError("graphics_opacity must be in the range (0, 1]")

    lines = [
        "# Generated by bias_vizualization. Geometry was calculated in Python.",
        f"source {_tcl_string(renderer_path)}",
        (
            f"set m [::bias_vizualization::prepare {_tcl_string(receptor_path)} "
            f"{_tcl_string(scene_name)} {_format_number(graphics_opacity)}]"
        ),
        "",
    ]
    lines.extend(render_primitive(primitive) for primitive in scene.objects)
    lines.append("")
    lines.extend(f"puts {_tcl_string(message)}" for message in scene.report)
    lines.extend(("mol top $m", ""))
    return "\n".join(lines)
